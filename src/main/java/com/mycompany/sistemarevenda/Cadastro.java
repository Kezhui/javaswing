    private void adicionarTabela(String marca, String modelo, String cor, int ano, String placa, String fornecedor, double valor_compra, String data_compra){
        ( (DefaultTableModel) Table_Lista.getModel() ).addRow( new Object[8] );
        
        int linha = Table_Lista.getRowCount() - 1;
        int col = 0;
        
        Table_Lista.setValueAt(marca, linha, col++);
        Table_Lista.setValueAt(modelo, linha, col++);
        Table_Lista.setValueAt(cor, linha, col++);
        Table_Lista.setValueAt(ano, linha, col++);
        Table_Lista.setValueAt(placa, linha, col++);
        Table_Lista.setValueAt(fornecedor, linha, col++);
        Table_Lista.setValueAt(valor_compra, linha, col++);
        Table_Lista.setValueAt(data_compra, linha, col++);
        ComboBox_Direcao.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "teste", "Elétrica", "Hidraulica", "Mecanica" }));
        limparCampos();
        
    }
    
